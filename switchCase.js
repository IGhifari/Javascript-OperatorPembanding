const day = "Senin";

switch(day){
    case "Senin":
        console.log("Senin");
        break;
    case 2:
        console.log("Selasa");
        break;
    case 3:
        console.log("Rabu");
        break;
    case 4:
        console.log("Kamis");
        break;
    default:
        console.log("Hari tidak valid");
}
